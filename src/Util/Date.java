public class Date {
        int dia;
        int mes;
        public Date(int dia, int mes) {
            this.dia = dia;
            this.mes = mes;
        }
        public int getDia() {
            return dia;
        }

        public void setDia(int dia) {
            this.dia = dia;
        }

        public int getMes() {
            return mes;
        }

        public void setMes(int mes) {
            this.mes = mes;
        }
}

